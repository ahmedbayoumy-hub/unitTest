class MathOperations {
    
    func addNumbers (x: Int,y: Int) -> Int {
        return x + y
    }
    
    func subtractNumbers (x: Int,y: Int) -> Int {
        return x - y
    }
    
    func multiplyNumbers (x: Int,y: Int) -> Int {
        return x * y
    }
    
    func divideNumbers (x: Int,y: Int) -> Int {
        return x / y
    }
    
    
}
